#Program Luas_Lingkaran
#nilai variable jari-jari diinput oleh user
ananda_jari_jari = float(input("MAsukkan jari-jari lingkaran: "))

#Kamus
pi = 3.14159

#proses menghitung luas lingkaran
ananda_luas = pi * ananda_jari_jari ** 2

#menampilkan hasil perhitungan luas lingkaran
print("Luas lingkaran dengan jari-jari", ananda_jari_jari, "adalah", ananda_luas)